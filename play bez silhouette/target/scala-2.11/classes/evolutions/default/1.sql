CREATE TABLE USERS (
    "ID"        INTEGER PRIMARY KEY AUTOINCREMENT,
    "USERID"    TEXT,
    "FIRSTNAME" TEXT,
    "LASTNAME"  TEXT,
    "EMAIL"     TEXT,
    "PROVIDER"  TEXT
);

